package com.example.yaqa.model;

public class QuestionSet {
    public String name;
    public String desc;
    public int number;
    public String author;

    public QuestionSet(String name, String desc, int number, String author) {
        this.name = name;
        this.desc = desc;
        this.number = number;
        this.author = author;
    }
}
